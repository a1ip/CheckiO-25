def count_neighbours(grid, row, col):
    result = 0

    for r in range(-1, 2):
        for c in range(-1, 2):
            if (0 <= row + r <= len(grid) - 1 and 0 <= col + c <= len(grid[0]) - 1):
                if (grid[row + r][col + c] == 1) and not (r == 0 and c == 0):
                    result += 1

    return result


